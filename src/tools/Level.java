package tools;

public class Level {

    int myLevel;
    
    private static final int STARTING_LEVEL = 1;
    
    public Level() {
        myLevel = STARTING_LEVEL;
    }
    
    public void setLevel(int theLevel) {
        myLevel = theLevel;
    }
    
    public int getLevel() {
        return myLevel;
    }
}
